# prompt_builder.py

def build_prompt(data_item: str, usage: str, kb_text: str) -> str:
    prompt = f"""你是一个帮助判断敏感个人信息的助手。以下是知识库内容：
-----
{kb_text}
-----
现在用户提供了一个待判断的数据项和使用场景，请判断该项是否属于敏感个人信息，并说明依据的理由。
如果数据项存在特殊实践争议（例如身份证号码、身份证号），可引用《个人信息保护法》第28条进行场景化分析：
- 一旦泄露或非法使用，是否易导致人格尊严受损？
- 一旦泄露或非法使用，是否易危害人身安全？
- 一旦泄露或非法使用，是否易危害财产安全？

【数据项】{data_item}
【使用场景】{usage}

【回答要求】
请结合知识库内容，判断用户输入的“数据项”在给定的“使用场景”中是否属于敏感个人信息。
1. 结论
2. 依据和理由
"""
    return prompt
